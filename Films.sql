Create database Cinema
USE Cinema

Create sequence Users_seq;

Create table Users(
UIdd int DEFAULT NEXTVAL ('Users_seq') PRIMARY KEY,
USurname varchar(255),
UName varchar(255),
UOtchestvo varchar(255),
UDate date,
UNickname varchar(255),
ULanguage varchar(255));

Create sequence Cinemastudio_seq;

Create table Cinemastudio(
CIdCinemastudio int DEFAULT NEXTVAL ('Cinemastudio_seq') PRIMARY KEY,
CName varchar(255),
CCountry varchar(255),
Ccity varchar(255));

Create sequence Film_seq;

Create table Film(
FIdFilm int DEFAULT NEXTVAL ('Film_seq') PRIMARY KEY,
FIdCinemaStudio int,
FName varchar(255),
FYear int,
FJanr varchar(255),
FMoney money,
CONSTRAINT FilIdCinema FOREIGN KEY (FIdCinemastudio) REFERENCES Cinemastudio(CIdCinemastudio));

Create sequence Rating_seq;

Create table Rating(
RIdRating int DEFAULT NEXTVAL ('Rating_seq') PRIMARY KEY,
RIdFilm int ,
RIdUsers int,
RDate date,
ROcenka int,
CONSTRAINT RaId FOREIGN KEY (RIdUsers) REFERENCES Users (UIdd),
CONSTRAINT RaId2 FOREIGN KEY (RIdFilm) REFERENCES Film (FIdFilm));

